    private void modificarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarbtnActionPerformed
        // TODO add your handling code here:
        int index=this.listaparq.getSelectedRow();
        if(index<0){
            JOptionPane.showMessageDialog(this, "Seleccione un Parqueadero");
        }
        else{
            String codigo=(String) this.listaparq.getValueAt(index, 0);
            ps.setModificar(true);
            ps.cargarParqueadero(codigo);
            DatosParqueaderoGUI datosParqueaderoGUI=new DatosParqueaderoGUI();
            this.setVisible(false);
            datosParqueaderoGUI.setVisible(true);
        }
    }//GEN-LAST:event_modificarbtnActionPerformed

