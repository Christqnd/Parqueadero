    private void Limpiar(DefaultTableModel d) {
        int b = d.getRowCount() - 1;  //Ãndices van de 0 a n-1
        for (int i = b; i >= 0; i--) {
            d.removeRow(i);
        }
    }
    private void mostrarDatos(){
        this.Limpiar(model1);
        List<Parqueadero> lista=new ArrayList();
        lista=cs.obtenerParqueaderos();
        for(Parqueadero p:lista){
            this.model1.addRow(new Object[]{p.getCodigo(),p.getNumero(),p.getPuertas().size(),p.getCapacidad()});
        }
    }

